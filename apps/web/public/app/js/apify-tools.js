export function apifyActorHref(actor) {
  if (actor.actor) return `https://apify.com/${actor.actor}`;
  return `https://apify.com/store?search=${encodeURIComponent(actor.storeQuery || actor.name)}`;
}

export function apifyActorsByLane() {
  return APIFY_LANES.map((lane) => ({
    ...lane,
    actors: APIFY_ACTORS.filter((actor) => actor.lane === lane.id),
  }));
}

export function apifyActorsByCategory() {
  return APIFY_ACTORS.reduce((groups, actor) => {
    (groups[actor.category] ||= []).push(actor);
    return groups;
  }, {});
}
